def plot_mpi_omp_grid(data, reso, fig_name='mpi_omp_grid', show_overhead=False):
    """
    Create a 2D heatmap of execution time:
    - X axis: OpenMP threads
    - Y axis: MPI processes per node
    Code mostly generated by chatgpt
    """

    # Step 1: aggregate results
    grid = defaultdict(list)

    for entry in data:
        if entry["resolution"]!=reso:
            continue
        mpi = entry["mpi_procs_per_node"]
        omp = entry["omp_threads"]

        avg_time, err_min, err_max = process_times(entry["timings"])
        grid[(mpi, omp)].append(avg_time)

    # Step 2: get sorted unique values
    mpi_vals = sorted(set(k[0] for k in grid.keys()))
    omp_vals = sorted(set(k[1] for k in grid.keys()))

    # remove entries that have either 1 MPI process or 1 OMP thread
    # these are for checking overhead
    if not show_overhead:
        if 1 in mpi_vals: mpi_vals.remove(1)
        if 1 in omp_vals: omp_vals.remove(1)

    # Step 3: build matrix
    matrix = np.full((len(mpi_vals), len(omp_vals)), np.nan)

    for i, mpi in enumerate(mpi_vals):
        for j, omp in enumerate(omp_vals):
            if (mpi, omp) in grid:
                matrix[i, j] = np.mean(grid[(mpi, omp)])

    # Step 4: plot heatmap
    fig, ax = plt.subplots(figsize=(6, 5))

    cmap = plt.cm.RdYlGn_r  # green = fast, red = slow
    #im = ax.imshow(matrix, cmap=cmap, aspect='auto')
    im = ax.imshow(matrix, cmap=cmap, norm=colorsx.LogNorm())

    # Step 5: ticks & labels
    ax.set_xticks(range(len(omp_vals)))
    ax.set_yticks(range(len(mpi_vals)))

    ax.set_xticklabels(omp_vals)
    ax.set_yticklabels(mpi_vals)

    ax.set_xlabel("OpenMP threads per MPI")
    ax.set_ylabel("MPI processes")
    #ax.set_title("Execution time heatmap")

    # Step 6: annotate each cell
    for i in range(len(mpi_vals)):
        for j in range(len(omp_vals)):
            val = matrix[i, j]
            if not np.isnan(val):
                ax.text(j, i, f"{val:.2f}",
                        ha="center", va="center",
                        color="black", fontsize=8)

    # Step 7: colorbar
    divider = make_axes_locatable(ax)
    cax = divider.append_axes("right", size="5%", pad=0.05)
    cbar = plt.colorbar(im, cax=cax)
    cbar.set_label("execution time")
    cbar.set_ticks([])

    plt.tight_layout()
    plt.savefig(fig_name, dpi=150)

''' Plot the strong scaling for MPI+OpenMP versus MPI-only'''
def make_plot_openmp(data, reso, arr_nodes, outname='scaling_openmp.png', title=None):

    labels=['MPI only', 'MPI + 2 OpenMP','MPI + 4 OpenMP','MPI + 8 OpenMP', 'MPI + 16 OpenMP']
    #labels=['MPI only']#, 'MPI + 4 OpenMP']

    # create colors
    cmap = plt.get_cmap('Greens')
    cNorm  = colorsx.Normalize(vmin=0.25, vmax=4.75)
    colorVals = {}
    colorVals = [(0.1,0.1,0.7)]
    for val in [1,2,3,4]:
        colorVals.append(cmap(cNorm(val)))

    fig, axes = plt.subplots(nrows=1, ncols=1, figsize=(4.5,4.5))

    ref_value = 1
    for omp,lab,col in zip([0,2,4,8,16],labels,colorVals):
        times = []
        for nodes in arr_nodes:
            for entry in data:
                if entry['resolution']!=reso:
                    continue
                if entry['nodes']!=nodes:
                    continue
                if entry['omp_threads']!=omp:
                    continue
                # reduce time data
                time, error_min, error_max = process_times(entry['timings'])
                times.append(float(time))
                if omp==0 and nodes==1:
                    ref_value=float(time)
        axes.plot(arr_nodes,ref_value/np.array(times),label=lab,color=col,marker='o',markersize=3)

    axes.plot(arr_nodes,arr_nodes,color='black',lw=1,ls='--')

    axes.set_xscale('log')
    axes.set_yscale('log')
    axes.set_xlabel('nodes')
    axes.set_ylabel('speedup')
    axes.set_xticks([])
    axes.set_xticks([],minor=True)
    axes.set_xticks(arr_nodes, labels=arr_nodes)
    axes.legend()
    if title!=None:
        axes.set_title(title)
    plt.savefig(outname, bbox_inches='tight', dpi=200)
    plt.close()
    print('Figure outputted to', outname)


''' Spit out a latex table comparing different number of openMP threads '''
def make_table_openmp(data, reso, arr_nodes):

    nthr = [0,2,4,8,16]
    space_report_string = 'Nodes & MPI '
    for i in range(1,len(nthr)):
        space_report_string = space_report_string + f'& omp {nthr[i]}'
    space_report_string = space_report_string + '& diff'
    print(space_report_string)

    ref = 0
    for nodes in arr_nodes:
        times = []
        for omp in nthr:
            for entry in data:
                if entry['resolution']!=reso:
                    continue
                if entry['nodes']!=nodes:
                    continue
                if entry['omp_threads']!=omp:
                    continue
                # reduce time data
                time, error_min, error_max = process_times(entry['timings'])
                times.append(float(time))

        if nodes==1:
            ref = min(times)

        # nodes MPI 2th 4th 8thr gain
        space_report_string = '{} & {:.3f}'.format(str(nodes).rjust(2),times[0])
        best_time = 1e10
        if len(times)>1:
            for i in range(1,len(nthr)):
                best_time = min(best_time,times[i])
                space_report_string = space_report_string + '& {:.3f}'.format(times[i])
            diff = (-1)*(best_time - times[0])/times[0] * 100
            space_report_string = space_report_string + '& {:.1f} \\\\ \\hline'.format(diff)
        print(space_report_string)

    print('scaling efficiencity at', arr_nodes[-1], 'nodes:', ref/times[1]/arr_nodes[-1])

